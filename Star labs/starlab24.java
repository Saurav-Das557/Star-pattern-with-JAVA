import java.util.Scanner;
public class starlab24{
  public static void main(String[] args){
    Scanner sc=new Scanner(System.in);
    int x=sc.nextInt();
    for(int count=1;count<=x;count++){
      System.out.print(count);
   }for(int count=x-1;count>=1;count--){
        System.out.print(count);
   }
  }
}

    
  